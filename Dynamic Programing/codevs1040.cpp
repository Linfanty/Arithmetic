#include <cstdio>
#include <cstring>
#include <iostream>
#include <algorithm>
#include <string>
using namespace std;
int main()
{
    int n,p,k,s;
    string a,dic;
    scanf("%d",&n);
    while(n--){
        a.clear();
        dic.clear();
        scanf("%d%d",&p,&k);
        for(int i=1;i<=p;i++){
            cin>>a[i];
        }
        scanf("%d",&s);
        for(int i=1;i<=s;i++){
            cin>>dic[i];
        }

    }
}
